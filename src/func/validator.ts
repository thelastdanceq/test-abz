export const validatePhone = (num: string) => {
    const time = num.trim();
    const pattern = /^\+380([0-9]{9})$/

    return pattern.test(time)
}

export const validateName = (name: string) => {
    const time = name.trim();
    return time.length >= 2 && time.length <= 60

}

